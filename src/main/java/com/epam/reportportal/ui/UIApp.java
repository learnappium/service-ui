package com.epam.reportportal.ui;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.boot.context.embedded.ConfigurableEmbeddedServletContainer;
import org.springframework.boot.context.embedded.EmbeddedServletContainerCustomizer;
import org.springframework.boot.web.servlet.ErrorPage;
import org.springframework.cloud.client.discovery.EnableDiscoveryClient;
import org.springframework.http.CacheControl;
import org.springframework.http.HttpStatus;
import org.springframework.stereotype.Component;
import org.springframework.web.servlet.config.annotation.ResourceHandlerRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurerAdapter;

/**
 * Gateway Application
 *
 * @author Andrei Varabyeu
 */
@SpringBootApplication
@EnableDiscoveryClient
public class UIApp extends WebMvcConfigurerAdapter {

	public static void main(String[] args) throws Exception {
		SpringApplication.run(UIApp.class, args);
	}

	@Override
	public void addResourceHandlers(ResourceHandlerRegistry registry) {
		/* do not cache htmls */
		registry.addResourceHandler("*.html", "*.htm").addResourceLocations("classpath:/public/").setCacheControl(CacheControl.noCache());

		/* non-expiring cache for other statics */
		registry.addResourceHandler("/**").addResourceLocations("classpath:/public/").setCachePeriod(Integer.MAX_VALUE);

		if (!registry.hasMappingForPattern("/webjars/**")) {
			registry.addResourceHandler("/webjars/**").addResourceLocations("classpath:/META-INF/resources/webjars/");
		}
	}

	@Component
	public class WebConfiguration implements EmbeddedServletContainerCustomizer {
		@Override
		public void customize(ConfigurableEmbeddedServletContainer configurableEmbeddedServletContainer) {
			configurableEmbeddedServletContainer.addErrorPages(new ErrorPage(HttpStatus.NOT_FOUND, "/404.html"));
		}
	}

}
